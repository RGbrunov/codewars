# Very simple, given an integer or a floating-point number, find its opposite.
#
# Examples:
#
# 1: -1
# 14: -14
# -34: 34

def opposite(number):
    # your solution here
    return -number


number_1 = 1
print(opposite(number_1))
number_2 = 14
print(opposite(number_2))
number_3 = -34
print(opposite(number_3))

